package dev.inoyu.openapi.enrich.maven;

/**
 * Maps a servlet class (simple or FQCN) to an absolute HTTP path.
 * Prefer this over {@code Map} parameters so FQCNs with dots are valid Maven XML.
 */
public class ServletPathMapping {

    private String className;
    private String path;

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
